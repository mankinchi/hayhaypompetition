<script type="text/javascript">
    $(".header-image").innerHeight($(window).innerHeight() - $(".nav-menu-top").innerHeight());
    $(".part").last().css('border', 'none');
    $(".content-navigation li").last().css('border', 'none');

    $(".more").click(function(event) {
        $("html, body").animate({
            'scrollTop': $(".content").eq(0).offset().top
        }, 400);
    });

    var limitArray = [];
    var endPoint = 0;
    $(".part").each(function(index, el) {
        var limit = $(el).innerHeight() / 2;
        if (endPoint == 0) {
            limit += $(el).offset().top;
            endPoint = $(el).offset().top + $(el).innerHeight();
        } else {
            limit += endPoint;
            endPoint += $(el).innerHeight();
        }
        limitArray.push(limit);
    });

    var currentPos = -1;

    function setContentNavigation(partNo) {
        $(".content-navigation li.active").removeClass('active');
        $(".content-navigation li").eq(partNo).addClass('active');
    }

    function moveContentNavigation() {
        var windowOffset = $(window).scrollTop();
        var contentOffsetTop = $(".content").offset().top;
        var contentOffsetBottom = $(".content").offset().top + $(".content").innerHeight() - 500;

        if (windowOffset >= contentOffsetTop && windowOffset <= contentOffsetBottom) {
            $(".content-navigation").stop();
            $(".content-navigation").animate({
                'top': windowOffset - contentOffsetTop
            }, 500, 'linear');
        }
    }

    $(".content-navigation li").each(function(index, el) {
        $(el).click(function(event) {
            setContentNavigation(index);
            $('html, body').animate({
                scrollTop : $(".part").eq(index).offset().top
            }, 500, 'linear', function() {
                moveContentNavigation();
            })
        });
    });

    $(window).on('wheel', function(event) {
        if (event.originalEvent.deltaY < 0) {
            // Scroll up
            var checkPos = $(window).scrollTop();
            if (currentPos > 0) {
                if (checkPos <=limitArray[currentPos - 1]) {
                    currentPos -= 1;
                    setContentNavigation(currentPos);
                }
            }
        } else {
            // Scroll down
            var checkPos = $(window).scrollTop() + $(window).innerHeight();
            if (currentPos < 5) {
                if (checkPos >=limitArray[currentPos + 1]) {
                    currentPos += 1;
                    setContentNavigation(currentPos);
                }
            }
        }
        moveContentNavigation();
    });
</script>
